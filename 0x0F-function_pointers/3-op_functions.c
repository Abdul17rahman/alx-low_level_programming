#include "calc.h"

/**
 * op_add - Adds two intergers
 * @a: int one
 * @b: int two
 *
 * Description: This function adds two numbers
 * Return: interger sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Subtracts two intergers
 * @a: int one
 * @b: int two
 *
 * Description: This function subtracts two numbers
 * Return: interger sub
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplys two intergers
 * @a: int one
 * @b: int two
 *
 * Description: This function multiplys two numbers
 * Return: interger product
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two intergers
 * @a: int one
 * @b: int two
 *
 * Description: This function divides two numbers
 * Return: interger division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Finds the reminder of two nums
 * @a: int one
 * @b: int two
 *
 * Description: This func uses the modulo operator
 * Return: interger reminder
 */

int op_mod(int a, int b)
{
	return (a % b);
}
